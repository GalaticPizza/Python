#OPERACIONES MATEMATICAS

import math

numero = int(input("ingrese un numero:"))

print(f'Raiz de {numero} = {math.sqrt(numero)}')
print(f'Seno de {numero} = {math.sin(numero)}')
print(f'Exponencial e de {numero} = {math.exp(numero)}')
print(f'Logartimo natural de euler {numero} = {math.log(math.exp(numero))}')
